function createData(rank, name, points){
    return {
        rank,
        name,
        points,
    };
}
function compareDescending(a, b, orderBy) {
    if (b[orderBy] < a[orderBy]) {
        return -1;
    }
    if (b[orderBy] > a[orderBy]) {
        return 1;
    }
    return 0;
}
function getComparator(order, orderBy) {
    return order === 'desc'
        ? (a, b) => compareDescending(a, b, orderBy)
        : (a, b) => -compareDescending(a, b, orderBy);
}


